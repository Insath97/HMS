    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String DriverID =  jTextField1.getText();
        String Name = jTextField3.getText();
        String age =  jTextField2.getText();
        String gender = null;
            if(jRadioButton1.isSelected()){
                gender = "Male";
            }
            else if (jRadioButton2.isSelected()){
                gender = "Female";
            }            
        String nic = jTextField5.getText();
        String company = (String)jComboBox2.getSelectedItem();
        String model = (String)jComboBox1.getSelectedItem();
        String available = (String)jComboBox3.getSelectedItem();
        String location = jTextField4.getText();  
      
        //database coonection
        Dconnection c = new Dconnection();

        try{
            String str = "insert into driver values('"+DriverID+"','"+Name+"','"+age+"','"+gender+"','"+nic+"','"+company+"','"+model+"','"+available+"','"+location+"')"; 
            c.s.executeUpdate(str);
            JOptionPane.showMessageDialog(null, "New Driver Added");
            clear();
        }
        catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Can't Added! Please Check All Details"); 
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

