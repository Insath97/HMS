    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int row=jTable1.getSelectedRow();
        String Table_click=(jTable1.getModel().getValueAt(row,0).toString());
           
        //database connection
        Dconnection c = new Dconnection();
        
        try{
            String str = "select * from driver where DriverID = '"+Table_click+"'";
            ResultSet rs = c.s.executeQuery(str);
                 
            if(rs.next()){
              jTextField1.setText(rs.getString("DriverID"));
              jTextField3.setText(rs.getString("name"));
              jTextField2.setText(rs.getString("age"));
              
              String add2 = rs.getString("gender");
              if("Male".equals(add2)){
                  jRadioButton1.setSelected(true);
              }
              else if("Female".equals(add2)){
                  jRadioButton2.setSelected(true);
              }

              jTextField5.setText(rs.getString("nic")); 
              
              String add3 = rs.getString("company");
              jComboBox2.setSelectedItem(add3);
              String add4 = rs.getString("model");
              jComboBox1.setSelectedItem(add4);
              String add5 = rs.getString("Available");
              jComboBox1.setSelectedItem(add5);
              
              jTextField4.setText(rs.getString("location"));
           }           
        }
        catch(Exception e){ 
            System.out.println(e);
        }       
    }//GEN-LAST:event_jTable1MouseClicked

